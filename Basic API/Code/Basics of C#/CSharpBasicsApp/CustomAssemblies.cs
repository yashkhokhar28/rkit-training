namespace CSharpBasicsApp;

/// <summary>
/// Provides basic math operations.
/// </summary>
public class CustomAssemblies
{
    /// <summary>
    /// Adds two numbers.
    /// </summary>
    public int Add(int a, int b)
    {
        return a + b;
    }

    /// <summary>
    /// Multiplies two numbers.
    /// </summary>
    public int Multiply(int a, int b)
    {
        return a * b;
    }
}